import math
import random

print(math.pow(10, 2)) # Multiplicamos el primer valor X veces segun el segundo valor
print(pow(10, 2))
print(math.sqrt(64)) # Sacamos la raiz cuadrada del valor indicado
print(math.isqrt(64)) # Raiz cuadrada resultado valor entero
print(math.sin(90)) # El seno
print(math.cos(90)) # El coseno
print(math.tan(64)) # la tangente
print(math.factorial(5)) # 1 * 2 * 3 * 4 * 5

print(random.randint(1, 100))